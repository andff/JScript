//Operadores Aritméticos


// '+' Adição
console.log("Adição: ", 5 + 5)

// '-' Subtração
console.log("Subtração: ", 10 - 7)

// '/' Divisão
console.log("Divisão: ", 15 / 3)

// '*' Multiplicação
console.log("Multiplicação: ", 20 * 3)

// '**' Exponencial
console.log("Exponencial: ", 5 ** 2)

// '%' Resto da divisão
console.log("Resto da divisão: ", 5 % 2)


// Aritimético atribuição
var numero = 1
